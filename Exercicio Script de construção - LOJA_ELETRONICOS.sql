CREATE DATABASE LOJA_ELETRONICOS;


CREATE TABLE TB_PRODUTOS (
    PRO_ID INT AUTO_INCREMENT PRIMARY KEY,
    PRO_NOME VARCHAR(100) NOT NULL,
    PRO_CATEGORIA VARCHAR(50),
    PRO_PRECO DECIMAL(10, 2),
    PRO_QUANTIDADE_ESTOQUE INT
);


CREATE TABLE TB_CLIENTES (
    CLI_ID INT AUTO_INCREMENT PRIMARY KEY,
    CLI_NOME VARCHAR(100) NOT NULL,
    CLI_EMAIL VARCHAR(100) NOT NULL,
    CLI_TELEFONE VARCHAR(15),
    CLI_ENDERECO VARCHAR(255)
);


CREATE TABLE TB_METODOS_PAGAMENTO (
    MEP_ID INT AUTO_INCREMENT PRIMARY KEY,
    MEP_TIPO VARCHAR(20) NOT NULL
);


CREATE TABLE TB_PEDIDOS (
    PED_ID INT AUTO_INCREMENT PRIMARY KEY,
    PED_CLI_ID INT,
    PED_DATA DATE,
    PED_MEP_ID INT,
    PED_VALOR_TOTAL DECIMAL(10, 2),
    FOREIGN KEY (PED_CLI_ID) REFERENCES TB_CLIENTES(CLI_ID),
    FOREIGN KEY (PED_MEP_ID) REFERENCES TB_METODOS_PAGAMENTO(MEP_ID)
);


CREATE TABLE TB_ITENS_PEDIDO (
    ITP_ID INT AUTO_INCREMENT PRIMARY KEY,
    ITP_PED_ID INT,
    ITP_PRO_ID INT,
    ITP_QUANTIDADE INT,
    ITP_PRECO_UNITARIO DECIMAL(10, 2),
    FOREIGN KEY (ITP_PED_ID) REFERENCES TB_PEDIDOS(PED_ID),
    FOREIGN KEY (ITP_PRO_ID) REFERENCES TB_PRODUTOS(PRO_ID)
);

